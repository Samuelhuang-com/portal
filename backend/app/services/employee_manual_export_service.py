def generate_module_manuals(
    module_key: str,
    doc_types: Optional[list[str]] = None,
) -> dict:
    """
    產生指定模組的操作手冊文件。

    Args:
        module_key: 模組識別碼（需存在於 MODULE_REGISTRY）
        doc_types: 要產生的文件種類清單；若為 None 則產生全部

    Returns:
        {
          "module_key": str,
          "module_name": str,
          "generated_files": [str, ...],
          "export_path": str,
          "generated_at": datetime,
          "download_url": str
        }
    """
    if module_key not in MODULE_REGISTRY:
        raise ValueError(f"模組 '{module_key}' 不存在")

    module = MODULE_REGISTRY[module_key]
    types_to_gen = doc_types if doc_types else ALL_DOC_TYPES

    # 確認並建立輸出目錄
    out_dir = EXPORT_ROOT / module_key
    out_dir.mkdir(parents=True, exist_ok=True)

    generated_files = []
    now = twnow()

    for doc_type in types_to_gen:
        if doc_type not in DOC_GENERATORS:
            continue
        filename = f"{DOC_TYPE_MAP[doc_type]}.md"
        content = DOC_GENERATORS[doc_type](module, module_key)
        filepath = out_dir / filename
        filepath.write_text(content, encoding="utf-8")
        generated_files.append(filename)

    return {
        "module_key": module_key,
        "module_name": module["name"],
        "generated_files": generated_files,
        "export_path": str(out_dir),
        "generated_at": now,
        "download_url": f"/api/v1/employee-manual-export/download/{module_key}",
    }


def build_zip(module_key: str) -> Optional[bytes]:
    """
    將指定模組的輸出目錄打包成 ZIP bytes。
    若目錄不存在或無檔案則回傳 None。
    """
    out_dir = EXPORT_ROOT / module_key
    if not out_dir.exists():
        return None

    md_files = list(out_dir.glob("*.md"))
    if not md_files:
        return None

    import io
    buffer = io.BytesIO()
    with zipfile.ZipFile(buffer, "w", zipfile.ZIP_DEFLATED) as zf:
        for filepath in sorted(md_files):
            zf.write(filepath, arcname=f"{module_key}/{filepath.name}")
    buffer.seek(0)
    return buffer.getvalue()


def get_export_status(module_key: str) -> dict:
    """查詢指定模組的匯出狀態"""
    out_dir = EXPORT_ROOT / module_key
    if not out_dir.exists():
        return {"module_key": module_key, "has_export": False, "files": [], "generated_at": None}

    md_files = sorted(out_dir.glob("*.md"))
    if not md_files:
        return {"module_key": module_key, "has_export": False, "files": [], "generated_at": None}

    # 取最新修改時間作為產生時間
    latest_mtime = max(f.stat().st_mtime for f in md_files)
    generated_at = datetime.fromtimestamp(latest_mtime)

    return {
        "module_key": module_key,
        "module_name": MODULE_REGISTRY.get(module_key, {}).get("name", module_key),
        "has_export": True,
        "files": [f.name for f in md_files],
        "generated_at": generated_at,
        "download_url": f"/api/v1/employee-manual-export/download/{module_key}",
    }
