def _build_template() -> list[DailyInspectionTemplateRow]:
    """將 _RAW 展開成帶 rowSpan 計算的完整模板列表。"""

    # 計算每個 (floor, item) 的列數
    item_counts: dict[tuple, int] = {}
    floor_counts: dict[str, int] = {}
    for floor, item, *_ in _RAW:
        key = (floor, item)
        item_counts[key] = item_counts.get(key, 0) + 1
        floor_counts[floor] = floor_counts.get(floor, 0) + 1

    seen_items:  set[tuple] = set()
    seen_floors: set[str]   = set()
    result: list[DailyInspectionTemplateRow] = []

    for floor, item, check_content, result_options, minutes, source_tab in _RAW:
        ikey = (floor, item)
        item_first  = ikey  not in seen_items
        floor_first = floor not in seen_floors
        seen_items.add(ikey)
        seen_floors.add(floor)

        result.append({
            "floor":           floor,
            "item":            item,
            "check_content":   check_content,
            "result_options":  result_options,
            "minutes":         minutes,
            "source_tab":      source_tab,
            "item_first_row":  item_first,
            "floor_first_row": floor_first,
            "floor_row_count": floor_counts[floor],
            "item_row_count":  item_counts[ikey],
        })

    return result


DAILY_INSPECTION_TEMPLATE: list[DailyInspectionTemplateRow] = _build_template()
