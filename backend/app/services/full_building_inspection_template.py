STANDARD_MINUTES_MORNING: int = 50   # 早班
STANDARD_MINUTES_TOTAL:   int = 80   # 整體（早班 + 晚班 / 1H20分）


# ── rowSpan 計算 ──────────────────────────────────────────────────────────────

def _build_template() -> list[FullBuildingDailyTemplateRow]:
    # 統計各 floor 和 item 的列數
    from collections import Counter
    floor_counts: Counter = Counter()
    item_counts:  Counter = Counter()
    for floor, item, *_ in _RAW:
        floor_counts[floor] += 1
        item_counts[(floor, item)] += 1

    seen_floors: set[str] = set()
    seen_items:  set[tuple] = set()
    result: list[FullBuildingDailyTemplateRow] = []

    for floor, item, check_content, result_options, minutes, source_tab in _RAW:
        floor_first = floor not in seen_floors
        item_first  = (floor, item) not in seen_items
        seen_floors.add(floor)
        seen_items.add((floor, item))

        result.append(FullBuildingDailyTemplateRow(
            floor=floor,
            item=item,
            check_content=check_content,
            result_options=result_options,
            minutes=minutes,
            source_tab=source_tab,
            item_first_row=item_first,
            floor_first_row=floor_first,
            floor_row_count=floor_counts[floor],
            item_row_count=item_counts[(floor, item)],
        ))

    return result


FULL_BUILDING_DAILY_INSPECTION_TEMPLATE: list[FullBuildingDailyTemplateRow] = _build_template()
