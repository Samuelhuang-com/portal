SELECT
    c.room_no                                              AS 房號,
    CASE WHEN length(c.room_no) >= 4
         THEN substr(c.room_no, 1, 2) || 'F'
         ELSE substr(c.room_no, 1, 1) || 'F'
    END                                                    AS 樓層,
    (SELECT y FROM params) || '/' || (SELECT m FROM params) AS 查詢年月,
    '未執行'                                                AS 狀態
FROM canonical c
LEFT JOIN ihg_rm_master m
       ON m.room_no     = c.room_no
      AND m.maint_year  = (SELECT y FROM params)
      AND m.maint_month = (SELECT m FROM params)
WHERE m.room_no IS NULL          -- 沒有對應 master 記錄 = 整月未執行
ORDER BY (c.room_no + 0);        -- 依房號數值排序（自然含樓層順序）


